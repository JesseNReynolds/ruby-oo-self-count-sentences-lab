require 'pry'

class String

  def sentence?
    if self.end_with?(".")
      true
    else
      false
    end
  end

  def question?
    if self.end_with?("?")
      true
    else
      false
    end
  end

  def exclamation?
    if self.end_with?("!")
      true
    else
      false
    end
  end

  def count_sentences
    working = self.split(/[.!?]/)
    final = []
    final = working.reject { |entry| entry.length <= 0 }
    return final.length
  end
end
